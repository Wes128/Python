# logical operators
#     and,or,not
marks = 76
marks1 = 89
marks2 = 45
marks3 = 23
print(marks>marks1 and marks2<marks3)
print(marks<marks1 and marks2>marks3)
print(marks>marks1 and marks2>marks3)

print(marks>marks1 or marks2<marks3)
print(marks<marks1 or marks2>marks3)
print(marks>marks1 or marks2>marks3)
    # not is used for negation
print(not(marks>marks1 or marks2>marks3))

